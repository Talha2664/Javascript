// 2. Assignment Operators


let x = 5;      // simple assignment

x += 3;         // x = x + 3
console.log(x); // 8

x -= 2;         // x = x - 2
console.log(x); // 6

x *= 2;         // x = x * 2
console.log(x); // 12

x /= 4;         // x = x / 4
console.log(x); // 3

x %= 2;         // x = x % 2
console.log(x); // 1