// 3. Comparison Operators

let num = 10;
let str = "10";

console.log(num == str);   // true (values equal)
console.log(num === str);  // false (value + type not equal)
console.log(num != 5);     // true
console.log(num !== "10"); // true
console.log(num > 5);      // true
console.log(num < 20);     // true
console.log(num >= 10);    // true
console.log(num <= 8);     // false